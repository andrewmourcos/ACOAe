# Maze class to help with printing functions
class Maze():
    def __init__(self, maze):
        self.maze = maze # 2D Array
        self.colored = []
    
    def print(self):
        for i, row in enumerate(reversed(self.maze)):
            for j, col in enumerate(row):
                if [j, len(self.maze)-1-i] in self.colored:
                    print(". ", end="")
                else:
                    print(col, end=" ")
            print("")

    def color(self, path):
        self.colored = path


"""
    A* grid-search algorithm using manhattan distance
"""
def a_star(maze, start, goals):
    # Constants
    actions=[[0,1],[1,0],[0,-1],[-1,0]]
    numrows = len(maze)
    numcols = len(maze[0])

    open_list = [ [0, [start]] ]
    visited = {(start[0], start[1])}
    num_explored_nodes = 0

    def manhattan_dist(a, b):
        return abs(a[0] - b[0]) + abs(a[1] - b[1])

    while open_list:
        open_list = sorted(open_list, key=lambda x: x[0]) # Sort based on evaluation value

        # print(open_list)

        num_explored_nodes += 1
        curr_expanded = open_list.pop(0)
        curr_path = curr_expanded[-1]
        curr_node = [col, row] = curr_path[-1]

        if curr_node in goals:
            print("Found exit at ", curr_node, " after exploring ", num_explored_nodes, " nodes.")
            print("Path: ", curr_path)
            print("Cost: ", len(curr_path))
            return curr_path

        for dx, dy in actions:
            r, c = row + dy, col + dx

            if (r in range(numrows) and
                c in range(numcols) and
                maze[r][c] == 0 and
                (c,r) not in visited):

                new_path = curr_path + [[c,r]]
                # Compute new evaluation score f(n) = g(n) + h(n)
                new_cost = len(new_path)
                new_heuristic = min( manhattan_dist((c,r), goal) for goal in goals )
                open_list.append( [new_cost + new_heuristic, new_path] )

                visited.add((c,r))

    print("No exit found.")
    return []

    

