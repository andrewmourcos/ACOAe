def randomSearch(maze, start, goals, maxIters):
        i = 0
        curr_pos, prev_pos = start, start
        path=[curr_pos]
        actions = [(0, 1), (0, -1), (1, 0), (-1, 0)] # move up, down, right, left

        if start in goals:
            print("Started at an exit")
            return path

        while i < maxIters:
                # Figure out which actions are valid at current state
                valid_actions = []
                for a in actions:
                        potential_next_x = curr_pos[0] + a[0]
                        potential_next_y = curr_pos[1] + a[1]
                        if (0 <= potential_next_x <= 24) and (0 <= potential_next_y <= 24):
                                # print([potential_next_x, potential_next_y], prev_pos)
                                if [potential_next_x, potential_next_y] != prev_pos:
                                        if maze[potential_next_x][potential_next_y] != 1:
                                                valid_actions.append(a)

                if len(valid_actions) < 1:
                    print("Agent is trapped after %s iterations!" %(i))
                    return []

                # Select random action
                rand_action = random.choice(valid_actions)

                prev_pos = curr_pos
                # Update current position
                curr_pos = [ curr_pos[k] + rand_action[k] for k in range(len(rand_action)) ]
                path.append(curr_pos)
                i+=1

                if curr_pos in goals:
                        # Print exit and cost (number of iterations)
                        print("Found exit at (%d, %d) after %d iterations" %(curr_pos[0], curr_pos[1], i))
                        return(path)
                

        print("Could not find exit after %d iterations" %(i))

